#include <stdio.h>

int	ft_recursive_power(int nb, int power)
{
	if (power < 0)
		return (0);
	if (power == 0)
		return (1);
	if (power > 1)
	{
		nb *= ft_recursive_power(nb, power-1);
	}
	return (nb);
}
int	main(void)
{
	printf("%i\n", ft_recursive_power(2, 3));
	return (0);
}
