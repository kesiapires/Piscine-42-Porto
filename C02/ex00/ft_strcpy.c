#include <stdio.h>

char	*ft_strcpy(char *dest, char *src)
{
	int	i;

	i = 0;
	while (src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	return (dest);
}

int	main(void)
{
	char	text1[20] = "rainbow";
	char	text2[20] = "buiatchaka";

	ft_strcpy(text1, text2);
	printf("%s %s\n", text1, text2);
	return (0);
}
