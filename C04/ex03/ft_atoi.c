#include <stdio.h>

int	ft_atoi(char *str)
{
	int	sign = 1;
	int	result;

	while (*str == ' ' || *str >= 9 && *str <= 13 )
	{
		str++;
	}
	while (*str == '-' || *str == '+')
	{
		if (*str == '-')
		{
			sign *= -1;
		}
		str++;
	}
	result = 0;
	while (*str >= '0' && *str <= '9')
	{
		result = result * 10 + *str - '0';
		str++;
	}
	return (result * sign);
}
int	 main(void)
{
	printf("%d\n", ft_atoi("  ++--456"));
	return (0);
}

